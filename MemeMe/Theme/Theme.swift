import UIKit

extension UIColor {
    
    struct AppTheme {
        static var darkGrey: UIColor  {
            return UIColor(red: 0.2, green: 0.22, blue: 0.22, alpha: 1)
        }
        static var lightGrey: UIColor  {
            return UIColor(red: 0.23, green: 0.25, blue: 0.25, alpha: 1)
        }
    }
}
